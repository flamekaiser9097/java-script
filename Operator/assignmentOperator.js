// Assignment Operator
let a=10;
let b=4;
console.log('a = ',a,'b = ',b);

a +=4 //a=a+4
console.log('a+=4',a);

a -=3 //a=a-3
console.log('a-=3',a);

a *=3 //a=a*3
console.log('a*=3',a);

a /=3 //a=a/3
console.log('a/=3',a);

a %=3 //a=a%3
console.log('a%=3',a);

a **=3 //a=a**3
console.log('a**=3',a);